package entity;

import java.sql.Date;

/**
 *
 * @author ViruSs0209
 */
public class Blog {
    private int id;
    private String title, type, author, banner, content;
    private Date published;

    public Blog(int id, String title, String type, String author, String banner, String content, Date published) {
        this.id = id;
        this.title = title;
        this.type = type;
        this.author = author;
        this.banner = banner;
        this.content = content;
        this.published = published;
    }

    public Blog() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public String getBanner() {
        return banner;
    }

    public void setBanner(String banner) {
        this.banner = banner;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public Date getPublished() {
        return published;
    }

    public void setPublished(Date published) {
        this.published = published;
    }    
}
